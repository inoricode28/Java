public class Fecha6 {
    private int dia, mes, año;
    
    public Fecha6() {
        this.dia=1;
        this.mes=1;
        this.año=2000;
    }
    public Fecha6(int dia, int mes, int año) {
        this.dia=dia;
        this.mes=mes;
        this.año=año;
    }
    public String mostrarFecha() {
        return getDia()+"/"+getMes()+"/"+getAño();
    }
    public int getDia() {
        return dia;
    }    
    public void setDia(int dia) {
        this.dia=dia;
    }
    public int getMes() {
        return mes;
    }    
    public void setMes(int mes) {
        this.mes=mes;
    }
    public int getAño() {
        return año;
    }    
    public void setAño(int año) {
        this.año=año;
    }
}
